using System;

namespace Problem2_1
{
    class Program
    {
        static void Main(string[] args)
        {
            int a = 5;
            int b = 2;

            Console.WriteLine("{0} + {1} = {2}", a, b, a + 3);
            Console.WriteLine("{0} - {1} = {2}", a, b, a - 3);
            Console.WriteLine("{0} * {1} = {2}", a, b, a * b);
            Console.WriteLine("{0} / {1} = {2}", a, b, a / b);
            Console.WriteLine("{0} % {1} = {2}", a, b, a % b);
        }
    }
}
